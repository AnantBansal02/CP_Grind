void solve()
{
    ll n, m;cin>>n>>m;
    ll r = n + m;
    ll l = max(0LL, n - m);
    ll ans = l;
    for (ll bit = 31; bit >= 0; --bit) {
        if ((r & (1LL << bit)) and !(l & (1LL << bit))) {
            ans |= (1LL << (bit+1)) - 1;
            break;
        }
    }
    cout<<ans<<"\n";
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    // factorial(1e5,1e9+7);
    // sieve(1e5);
    ll t = 1;
    cin >> t;
    while(t--){
        solve();
    }
    return 0;
}